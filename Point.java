/* shoelace theorem*/
public class Point{
 private double xcoordinate;
 private double ycoordinate;
 
 public Point (double x, double y)
 {
  xcoordinate = x;
  ycoordinate = y;
 }
 
 public double getXcoordinate(){
  return xcoordinate; 
 }
 
 public double getYcoordinate(){
  return ycoordinate; 
 }
}